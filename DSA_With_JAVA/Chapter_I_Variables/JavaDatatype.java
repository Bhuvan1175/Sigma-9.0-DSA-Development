public class JavaDatatype {
    public static void main(String[] args) {
        // Primitive Data type
        byte b = 8;
        System.out.println(b);

        char ch = 'a';
        System.out.println(ch);

        boolean var = true;
        System.out.println(var);
        
        double a = 10.5;
        System.out.println(a);

    }
}

/*
 * SIZE OF DATA TYPE
 * 
 * byte : 1 bytes [-128 to 127]
 * short : 2 bytes
 * char : 2 bytes
 * boolean : 1 bytes
 * int : 4 bytes
 * long : 8 bytes
 * float : 4 bytes
 * double : 8 bytes
 *
 */