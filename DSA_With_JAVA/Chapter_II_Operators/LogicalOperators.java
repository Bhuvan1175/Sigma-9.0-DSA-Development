package DSA_With_JAVA.Chapter_II_Operators;

public class LogicalOperators {
    public static void main(String[] args) {
        System.out.println((3>2) && (5>0));
        System.out.println((3>2) && (5>0));

        System.out.println((3<2) || (5>0)); 
    }
}


// Logical Operator
//  && --> Logical AND
//  || --> Logical OR
//  !  --> Logical NOT